export function formatDate(dateString: string): string {
  return new Date(dateString).toLocaleString('en-US', {
    year: 'numeric',
    month: 'short',
    day: 'numeric',
    hour: '2-digit',
    minute: '2-digit'
  })
}

export function classNames(...classes: (string | boolean)[]): string {
  return classes.filter(Boolean).join(' ')
}